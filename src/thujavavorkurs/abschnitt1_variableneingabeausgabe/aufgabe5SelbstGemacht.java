package thujavavorkurs.abschnitt1_variableneingabeausgabe;

public class aufgabe5SelbstGemacht {
		  public static void main(String[] args) {
		  System.out.println("Apple Pie Receipt:");
		  System.out.println("==================");
		  // declarate variable "eggs" of type "int"
		  int eggs;
		  // assign value 4 to "eggs"
		  eggs = 4;
		  System.out.println("- " + eggs + " Eggs");
		  // declarate variable "milk" of type "double"
		  // and assign value 100 in one line
		  double milk = 100;
		  System.out.println("- " + milk + " ml Milk" );
		  // declarate variable "flour" of type "double"
		  double flour;
		  // assign value 200 to "flour"
		  flour = 200;
		  // add 150 to "flour"
		  flour = flour + 150;
		  System.out.println("- " + flour + " mg Flour");
		  // declarate variable "apple" of type "int"
		  int apple;
		  // assign the value of "eggs" to "apple"
		  apple = eggs;
		  System.out.println("- " + apple + " Apples");
		 	
	}
}
