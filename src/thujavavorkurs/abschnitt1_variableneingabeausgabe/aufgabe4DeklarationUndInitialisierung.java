package thujavavorkurs.abschnitt1_variableneingabeausgabe;

public class aufgabe4DeklarationUndInitialisierung {
		  public static void main(String[] args) {
		 
		  int number; //Variable 1
		  String word; //Variable 2
		  double realNumber; // Variable 3
		  number = 1; //Initialisierung 1
		  word = "Hello"; // Initialisierung 2
		  realNumber = 0.2; // Initialisierung 3
		  System.out.println(number);
		  System.out.println(word);
		  System.out.println(realNumber);
		  int anotherNumber = 5; //Variable 4
		  String anotherWord = "You"; //Variable 5 mit Deklaration
		  double anotherRealNumber = 8.15; //Initialisierung 4
		  System.out.println(anotherNumber);
		  System.out.println(anotherWord);
		  System.out.println(anotherRealNumber);

		  }
}
