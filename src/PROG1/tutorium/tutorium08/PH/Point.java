public class Point {

    // Eigenschaften/ Attribute:
    double x;
    double y;

    // Konstruktoren:
    public Point(int x, int y) {
        this.x = x;
        this.y = y;
    }

    // Methoden:
    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
