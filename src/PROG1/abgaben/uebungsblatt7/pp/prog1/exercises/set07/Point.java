public class Point {

    // Eigenschaften/ Attribute:
    double x;
    double y;

    // Konstruktoren:
    public Point(double x, double y) {
        this.x = x;
        this.y = y;
    }

    // Methoden:
    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
